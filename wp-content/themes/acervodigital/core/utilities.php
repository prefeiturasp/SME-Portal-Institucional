// Adicionar novo widget no dashboard
function add_dashboard_widgets() {
	wp_add_dashboard_widget(
		'one_dashboard_widget', // Slug do Widget admin
		'Titulo do Painel 1', // Titulo Widget admin
		'add_one_new_dashboard_widgets' // Nome da Função para mostrar o widget
	);
	wp_add_dashboard_widget(
		'two_dashboard_widget', // Slug do Widget admin
		'Titulo do Painel 2', // Titulo Widget admin
		'add_two_new_dashboard_widgets' // Nome da Função para mostrar o widget
	);
	wp_add_dashboard_widget(
		'three_dashboard_widget', // Slug do Widget admin
		'Titulo do Painel 3', // Titulo Widget admin
		'add_three_new_dashboard_widgets' // Nome da Função para mostrar o widget
	);
}
// Registra o widget no dashboard widget com 'wp_dashboard_setup'
add_action( 'wp_dashboard_setup', 'add_dashboard_widgets' );

